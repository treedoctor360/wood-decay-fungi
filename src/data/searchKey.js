// 針葉樹と判定するための宿主名の手がかり（部分一致）。
// これに該当しなければ広葉樹とみなす（緑化樹木の多くは広葉樹）。
const CONIFER_HINTS = [
  "マツ", "松", "スギ", "杉", "ヒノキ", "檜", "ヒバ", "アスナロ",
  "カラマツ", "モミ", "樅", "ツガ", "トウヒ", "トガサワラ", "イチイ",
  "カヤ", "イヌマキ", "コウヤマキ", "メタセコイア", "ナギ", "針葉",
];

// 宿主文字列が針葉樹らしいか。空なら null（不明）。
export function isConifer(host) {
  const h = (host || "").trim();
  if (!h) return null;
  return CONIFER_HINTS.some((k) => h.includes(k));
}

// 発生部位を「根株」か「幹・枝」に大別する。UIの4値に対応。
// 根株・幹基部 → 根株 / それ以外（幹下部・幹上部・枝）→ 幹・枝
export function partCategory(part) {
  const p = (part || "").trim();
  if (!p) return null;
  if (p.includes("根株") || p.includes("幹基部") || p.includes("地際")) return "根株";
  return "幹枝";
}

