// ------------------------------------------------------------
// 観察状況（host, part）から該当する検索表グループを返す。
// 針葉樹/広葉樹・根株/幹枝のどちらかでも不明なら、該当しうる群を広めに返す。
// ------------------------------------------------------------
export function relevantKeyGroups({ host, part }) {
  const conifer = isConifer(host);          // true / false / null
  const pcat = partCategory(part);          // "根株" / "幹枝" / null
  const hostType = conifer === null ? null : (conifer ? "針葉樹" : "広葉樹");

  return SEARCH_KEY_GROUPS.filter((g) => {
    const hostOk = hostType === null || g.hostType === hostType;
    const partOk = pcat === null || g.partType === pcat;
    return hostOk && partOk;
  });
}

// プロンプトに注入するテキストブロックを組み立てる。
// 該当群の原典テキスト（＋D群なら下位D1〜D3）を返す。
export function keyPromptBlock({ host, part }) {
  const groups = relevantKeyGroups({ host, part });
  if (groups.length === 0) return "";

  const blocks = groups.map((g) => {
    let t = `${g.title}\n${g.text}`;
    if (g.subKeys) {
      t += "\n\n" + g.subKeys.map((k) => `${SEARCH_KEY_SUBGROUPS[k].title}\n${SEARCH_KEY_SUBGROUPS[k].text}`).join("\n\n");
    }
    return t;
  });

  const scope = groups.length === SEARCH_KEY_GROUPS.length
    ? "（宿主・部位が未確定のため全群を提示）"
    : `（観察状況に該当する群のみ提示）`;

  return `# 図鑑の簡易検索表（該当群）${scope}\n` +
    `緑化樹木腐朽病害ハンドブック2017「主な木材腐朽菌の簡易検索表」より。\n` +
    `この分岐に沿って候補を絞ること。分岐条件と矛盾する種は、色やサイズが似ていても候補から外すか確信度を極小化する。\n` +
    `ただし観察情報が「不明・入力なし」の段は判断に使わない（減点しない）。\n\n` +
    blocks.join("\n\n");
}
